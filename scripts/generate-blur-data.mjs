#!/usr/bin/env node

/**
 * Generate blur placeholder data for images.
 * Uses sharp (bundled with Next.js) to create tiny blurred base64 images.
 *
 * Run: node scripts/generate-blur-data.mjs
 */

import { readdir, writeFile } from 'node:fs/promises';
import { join, relative, extname } from 'node:path';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const sharp = require('sharp');

const PUBLIC_DIR = join(import.meta.dirname, '..', 'public');
const OUTPUT_FILE = join(import.meta.dirname, '..', 'src', 'data', 'blurData.ts');

const IMAGE_EXTENSIONS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif']);
const SKIP_PATTERNS = ['web-app-manifest', 'og.jpg', 'favicon'];

async function findImages(dir, baseDir = dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const results = [];

  for (const entry of entries) {
    const fullPath = join(dir, entry.name);

    if (entry.isDirectory()) {
      results.push(...await findImages(fullPath, baseDir));
      continue;
    }

    const ext = extname(entry.name).toLowerCase();
    if (!IMAGE_EXTENSIONS.has(ext)) continue;
    if (SKIP_PATTERNS.some(p => entry.name.includes(p))) continue;

    const relativePath = '/' + relative(baseDir, fullPath);
    results.push({ relativePath, fullPath });
  }

  return results;
}

async function generateBlurDataUrl(imagePath) {
  const buffer = await sharp(imagePath)
    .resize(10, undefined, { fit: 'inside' })
    .blur(2)
    .jpeg({ quality: 20 })
    .toBuffer();

  return `data:image/jpeg;base64,${buffer.toString('base64')}`;
}

async function main() {
  console.log('Scanning for images...');
  const images = await findImages(PUBLIC_DIR);
  console.log(`Found ${images.length} images.`);

  const entries = [];

  for (const { relativePath, fullPath } of images) {
    try {
      const blurDataUrl = await generateBlurDataUrl(fullPath);
      entries.push({ path: relativePath, blurDataUrl });
      console.log(`  OK: ${relativePath} (${blurDataUrl.length} chars)`);
    } catch (err) {
      console.warn(`  SKIP: ${relativePath} (${err.message})`);
    }
  }

  const lines = entries
    .sort((a, b) => a.path.localeCompare(b.path))
    .map(e => `  '${e.path}': '${e.blurDataUrl}',`)
    .join('\n');

  const content = `// Auto-generated by scripts/generate-blur-data.mjs — do not edit manually.
export const blurDataMap: Record<string, string> = {
${lines}
};
`;

  await writeFile(OUTPUT_FILE, content, 'utf-8');
  console.log(`\nWrote ${entries.length} entries to ${OUTPUT_FILE}`);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
